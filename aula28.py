#AULA 28 - ESTRUTURA DE REPETIÇÃO WHILE (ENQUANTO)

# Definindo a mensagem a ser repetida
mensagem = "Eu estou aprendendo Python"

contador = 0  # Inicializando o contador

while contador < 10: # Loop while para repetir a mensagem 10 vezes
    
    print(mensagem)  # Exibindo a mensagem
    
    contador += 1  # Incrementando o contador
    
    
    